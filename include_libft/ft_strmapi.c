#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char	*res;
	size_t	len_s;
	size_t	i;

	if (s == NULL || f == NULL)
		return (NULL);
	len_s = ft_strlen(s);
	res = (char *)malloc(sizeof(char) * (len_s + 1));
	if (res == NULL)
		return (NULL);
	i = 0;
	while (s[i] != '\0')
	{
		res[i] = f(i, s[i]);
		i++;
	}
	res[i] = '\0';
	return (res);
}
